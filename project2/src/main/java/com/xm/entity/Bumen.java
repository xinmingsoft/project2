package com.xm.entity;

public class Bumen {
	
	private int id;
	private String bumenmc;
	private String bumenzw;
	private boolean isused;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBumenmc() {
		return bumenmc;
	}
	public void setBumenmc(String bumenmc) {
		this.bumenmc = bumenmc;
	}
	public String getBumenzw() {
		return bumenzw;
	}
	public void setBumenzw(String bumenzw) {
		this.bumenzw = bumenzw;
	}
	public boolean isUsed() {
		return isused;
	}
	public void setUsed(boolean isused) {
		this.isused = isused;
	}
	
}
